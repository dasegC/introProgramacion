public class ifStatment {
    public static void main(String[] args) {
        int numeroIf = 1;
        if (numeroIf > 0) {
            System.out.println("El número " + numeroIf + " es positivo");
        } else if (numeroIf < 0) {
            System.out.println("El número " + numeroIf + " es negativo");
        } else {
            System.out.println("El número ingresado es 0");
        }
    }
}
